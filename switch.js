let x=20;
let y=10;
let op="+";
switch(op){
    case "+":
        console.log("Sum is="+(x+y));
        break;
    case "-":
        console.log("Sub is="+(x-y));
        break;
    case "*":
        console.log("Multiplication is="+(x*y));
        break;
    case "/":
        console.log("Division is="+(x/y));
        break;
}